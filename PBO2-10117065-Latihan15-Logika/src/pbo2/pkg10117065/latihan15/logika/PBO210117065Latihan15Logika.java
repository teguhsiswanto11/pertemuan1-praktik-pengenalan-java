public class PBO210117065Latihan15Logika {

    public static void main(String[] args) {
         // deklarasi variabel
        boolean a = true;
        boolean b = false;
        
        //keluaran
        System.out.println("a && b = " + (a && b));
        System.out.println("a || b = " + (a || b));
        System.out.println("!a = " + !a);
        System.out.println("!(a && b) = " + !(a && b));
    }
    
}
